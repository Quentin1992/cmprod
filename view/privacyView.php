<h2>1.Collecte de l’information</h2>
<p>Nous recueillons des informations lorsque vous nous envoyez un message via le formulaire de la page « Contact » du site. Les informations recueillies incluent votre nom, votre adresse e-mail, et/ou numéro de téléphone.</p>
