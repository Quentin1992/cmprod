        <li>Vous envoyer occasionnellement des nouvelles de l’entreprise (exemple : nouveau service)</li>
    </ul>
</p>

<h2>3. Confidentialité</h2>
<p>Nous sommes les seuls propriétaires des informations recueillies sur ce site. Vos informations personnelles ne seront pas vendues, échangées, transférées, ou données à une autre société pour n’importe quelle raison.</p>
<h2>5. Protection des informations</h2>
<p>Nous mettons en œuvre une variété de mesures de sécurité pour préserver la sécurité de vos informations personnelles. Nous utilisons un cryptage pour protéger les informations sensibles transmises en ligne. Nous protégeons également vos informations hors ligne. Les ordinateurs et serveurs utilisés pour stocker des informations personnelles identifiables sont conservés dans un environnement sécurisé.</p>
<h3>Est-ce que nous utilisons des cookies ?</h3>
<p>Nous n’utilisons aucun cookie sur notre site.</p>
<h2>6. Se désabonner</h2>
<p>Nous utilisons l’adresse e-mail que vous fournissez pour vous envoyer des informations et mises à jour relatives à votre demande, des nouvelles de l’entreprise de façon occasionnelle. Si à n’importe quel moment vous souhaitez ne plus recevoir d’e-mails, des instructions de désabonnement détaillées sont incluses en bas de chaque e-mail.</p>
<h2>7. Consentement</h2>
<p>En utilisant notre site, vous consentez à notre politique de confidentialité.</p>
